#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

/*--------------------------------------main.cpp--------------------------------------*\
main function creates instances of Weapon class representing diff types of Weapons,
HumanA & HumanB objects representing characters with names & weapons. It invokes methods
on these objects to demonstrate their behavior.
\*------------------------------------------------------------------------------------*/

int main()
{
	{
		Weapon	club = Weapon("crude spiked club");

		HumanA	bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}

	{
		Weapon	club = Weapon("crude spiked club");

		HumanB	jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
		
	return (0);
}
